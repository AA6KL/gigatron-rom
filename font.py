def _char(*args):
  """Convert character from easy-readable strings data into ROM bytes"""
  X, Y = len(args[0]), len(args)
  bytes = []
  for x in range(X):
    byte = 0
    for y in range(Y):
      bit = 0 if args[y][x]=='.' else 1
      byte = 2*byte + bit
    bytes.append(byte)
  return bytes

